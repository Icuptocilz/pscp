'''ProgressiveTax'''
def main():
    '''ProgressiveTax'''
    money = float(input())
    if 150000 < money <= 300000:
        temp = (money - 150000)*(5/100)
    elif 300000 < money <= 500000:
        temp = (money - 300000)*(10/100) + (150000)*(5/100)
    elif 500000 < money <= 750000:
        temp = (money - 500000)*(15/100) + (200000)*(10/100) + (150000)*(5/100)
    elif 750000 < money <= 1000000:
        temp = (money - 750000)*(20/100) + (250000)*(15/100) + (200000)*(10/100) + (150000)*(5/100)
    elif 1000000 < money <= 2000000:
        temp = (money - 1000000)*(25/100) + (250000)*(20/100) + (250000)*(15/100) + \
            (200000)*(10/100) + (150000)*(5/100)
    elif 2000000 < money <= 4000000:
        temp = (money - 2000000)*(30/100) + (1000000)*(25/100) + (250000)*(20/100) + \
            (250000)*(15/100) + (200000)*(10/100) + (150000)*(5/100)
    elif money > 4000000:
        temp = (money - 4000000)*(35/100) + (2000000)*(30/100) + (1000000)*(25/100) + \
            (250000)*(20/100) + (250000)*(15/100) + (200000)*(10/100) + (150000)*(5/100)
    else:
        temp = 0
    print(int(temp))
main()
